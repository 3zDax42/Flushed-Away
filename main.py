def Level_Display(Level):
    x = 0; y = 0
    for i in Level:
        for j in i:
            if j == 1:
                pygame.draw.rect(Game_Screen, (200, 200, 200), (x*32, y*32, 32, 32))
            elif j == 2 or j == 3:
                pygame.draw.rect(Game_Screen, (100, 100, 255), (x*32, y*32, 32, 32))
            elif j == 4:
                pygame.draw.rect(Game_Screen, (160, 160, 160), (x*32, y*32, 32, 32))
            elif j == 5:
                pygame.draw.rect(Game_Screen, (100, 100, 100), (x*32, y*32, 32, 32))  
            elif j == 6:
                pygame.draw.rect(Game_Screen, (250, 250, 250), (x*32, y*32, 32, 32))
            x += 1
        x = 0; y += 1


